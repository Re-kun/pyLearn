# For

# Loop list

numbers = [1,2,3,4,5,6,7]

for i in numbers :
    print(i)

print("\n")

# Loop with list

for i in range(5) :
    print(i)

print("\n")

# Looping string
word = "Hello World"
for i in word :
    print(i)

# While

print("\n")

number = 0
print(f"number : {number}\n")

while number < 7 :
    number += 1
    print(f"number : {number}\n")